-- 删除已存在的数据库
DROP DATABASE IF EXISTS `CalmResponse`;

-- 创建数据库
CREATE DATABASE `CalmResponse`;

-- 使用数据库
USE `CalmResponse`;

-- 用户表
CREATE TABLE `user` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `username` varchar(255) NOT NULL COMMENT '用户名',
  `password` varchar(255) NOT NULL COMMENT '密码',
  `balance` double DEFAULT 0 COMMENT '余额',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `status` tinyint NOT NULL DEFAULT 0 COMMENT '状态（0正常1封号）',
  `permission` tinyint NOT NULL DEFAULT 0 COMMENT '权限（0用户 1管理员）',
  `is_delete` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除标记（0未删除 1已删除）',
  PRIMARY KEY (`id`)
) COMMENT='用户表';

-- 乘客表
CREATE TABLE `passenger` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `name` varchar(50) NOT NULL COMMENT '姓名',
  `id_card` varchar(18) NOT NULL COMMENT '身份证',
  `status` tinyint NOT NULL DEFAULT 0 COMMENT '状态（0成人 1学生）',
  `phone_number` varchar(20) NOT NULL COMMENT '电话号码',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `is_delete` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除标记（0未删除 1已删除）',
  PRIMARY KEY (`id`)
) COMMENT='乘客表';

-- 航班表
CREATE TABLE `flight` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `flight_number` varchar(20) NOT NULL COMMENT '航班号',
  `aircraft_model` varchar(50) NOT NULL COMMENT '飞机型号（波音747）',
  `seat_layout` varchar(255) NOT NULL COMMENT '座位布局--票数',
  `departure_city` varchar(50) NOT NULL COMMENT '起飞城市',
  `arrival_city` varchar(50) NOT NULL COMMENT '着陆城市',
  `departure_time` datetime NOT NULL COMMENT '起飞时间',
  `arrival_time` datetime NOT NULL COMMENT '着陆时间',
  `price` double NOT NULL COMMENT '价格',
  `total_seats` int NOT NULL COMMENT '票数',
  `remaining_seats` int NOT NULL COMMENT '余票数',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `airline` varchar(50) NOT NULL COMMENT '航空公司',
  `is_delete` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除标记（0未删除 1已删除）',
  PRIMARY KEY (`id`)
) COMMENT='航班表';

-- 已被预定的座位表
CREATE TABLE `booked_seat` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `flight_number` varchar(20) NOT NULL COMMENT '航班号',
  `seat_type` varchar(20) NOT NULL COMMENT '座位类型（头等舱/经济舱等）',
  `seat_number` varchar(10) NOT NULL COMMENT '座位号',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `is_delete` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除标记（0未删除 1已删除）',
  PRIMARY KEY (`id`)
) COMMENT='已被预定的座位表';

-- 总订单表
CREATE TABLE `order` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `username` varchar(255) NOT NULL COMMENT '用户名',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
  `price` double NOT NULL COMMENT '价格',
  `flight_number` varchar(20) NOT NULL COMMENT '航班号',
  `payment_method` varchar(20) NOT NULL COMMENT '支付方式',
  `status` tinyint NOT NULL DEFAULT 0 COMMENT '状态（0未支付 1已支付 2退款）',
  `service_fee` double NOT NULL DEFAULT 0 COMMENT '手续费（默认值0）',
  `seat_number` varchar(10) DEFAULT NULL COMMENT '座位号',
  `passenger_name` varchar(50) NOT NULL COMMENT '乘客姓名',
  `passenger_id_card` varchar(18) NOT NULL COMMENT '乘客身份证',
  `is_delete` tinyint NOT NULL DEFAULT 0 COMMENT '逻辑删除标记（0未删除 1已删除）',
  PRIMARY KEY (`id`)
) COMMENT='总订单表';
